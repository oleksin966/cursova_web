       	<li ><p>Груди бджоли, обладнані двома парними різновеликими крилами і трьома парами ніг. Крила бджоли один з одним з’єднані з допомогою дрібних гачків. Ноги бджоли покриті ворсинками, які служать для практичних цілей – чищення антен, зняття пластинок воску і т. д.</p></li>
