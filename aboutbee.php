	<p>Що стосується улюблених місць перебування цих комах, то вони люблять селитися в гірських ущелинах, влаштовувати свої вулики в дуплах старих дерев і земляних норах. Для бджіл важливо, щоб місце їх проживання було захищене від вітрів, і поблизу знаходилася водойма.</p>
