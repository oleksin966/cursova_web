<div align="center" style="padding: 30px" id="ex3">
	<h2>Спосіб життя бджіл</h2>
</div>
<div class="bee-text">
	<p>Бджоли – колективні комахи, які живуть великими сім’ями – вуликами і мають сувору ієрархію та розподіл праці. Склад бджолиної сім’ї включає в себе:</p>
	<ul>
		<li><p>матку,</p></li>
		<li><p>трутня,</p></li>
		<li><p>робочу бджолу.</p></li>
	</ul>
	<p>У бджолиному суспільстві панує матріархат і саме на самках повністю лежить забезпечення життя вулика, в той час як самці, вони ж трутні, існують лише для продовження роду.

Матка бджоли є царицею вулика, саме вона відповідальна за відтворення потомства, також саме вона є засновницею вулика і на перших порах займається його облаштуванням, поки в цьому її не замінюють робочі бджілки.</p>
<p>Завдання самців бджіл, трутнів, тільки одне – запліднити матку.

Все господарське життя вулика лежить на робочих бджолах, самках бджоли, нездатних до статевого розмноження. Саме вони ретельно збирають нектар з квітів, захищають вулик у разі небезпеки, займаються його облаштуванням, переносять мед і т. д.</p>
