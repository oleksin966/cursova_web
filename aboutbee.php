	<h2>Скільки живе бджола</h2>
</div>
<div class="bee-text">
	<p>Тривалість життя бджоли безпосередньо залежить від її місця в бджолиному суспільстві, а також часу появи на світ.

Скільки живе робоча бджола? Термін життя її не великий, і якщо вона народилася навесні або влітку, то зазвичай він складає в середньому всього лише місяць. Така коротка тривалість життя обумовлена важкою працею, яку веде робоча бджілка, добуваючи нектар.</p>
<p>Якщо ж робочій бджолі пощастило народитися восени, то вона може прожити навіть півроку, так як їй потрібно пережити зимові холоди, щоб навесні бути відповідальною за збір меду і взяти участь у його накопиченні.</p>
	<img src="img/bee-info5.jpg">
<p>Термін життя трутня ще більш короткий, ніж у робочої бджоли, через два тижні після появи на світ він вже стає здатним до запліднення матки, і що найцікавіше, через кілька днів після цього самого запліднення трутні зазвичай вмирають. Також буває так, що із закінченням періоду збору меду, і настанням зимових холодів робочі бджоли виганяють на цей момент вже не потрібних трутнів з вулика, після чого ті також гинуть.</p>
<p>Довше за всіх у бджолиному суспільстві живе матка бджола. Зазвичай середня тривалість життя матки становить 5-6 років, але для цього їй необхідно бути цінною самкою і регулярно давати новий приплід.</p>
</div>
<div align="center" style="padding: 30px" id="ex5">
	<h2>Чим харчується бджола</h2>
</div>
<div class="bee-text">
	<p>Бджоли дуже запасливі. Збираючи нектар, вони виробляють мед зовсім не для того, щоб порадувати людину. Таким чином вони добувають собі корм, при цьому роблячи запаси на зиму. Пилок з рослин бджоли також переробляють, у результаті чого виходить перга, так званий бджолиний хліб. Вона є дуже цінним білковим кормом для личинок, а після молочно-кислого бродіння корисна для людини, так як в її складі велику кількість амінокислот, органічних кислот, макро – і мікроелементів. При нестачі нектару і пилку бджола може збирати та інші продукти рослинного походження, наприклад, падь, сік дозрілих ягід. Харчування бджоли протягом року змінюється в залежності від кліматичних умов та інтенсивності медозбору.</p>
	<img src="img/bee-info6.jpg">
<p>Бджоли харчуються також квітковим пилком і нектаром. Через спеціальний хоботок нектар надходить в зоб, де переробляється в мед. Збираючи пилок і нектар, бджоли здійснюють дуже важливу і корисну функцію по запиленню квіток. У пошуках їжі бджоли можуть пролітати до 10 км за день.</p>
</div>
