<p>Характерною ознакою будови тіла медоносної бджоли, як і всіх комах, є зовнішній скелет. Це твердий покрив тіла (кутикула), що захищає ніжні органи порожнини. До скелета прикріплюються внутрішні органи бджоли. Разом з волосяним покривом зовнішній скелет захищає організм від несприятливих умов зовнішнього середовища. Забезпечуючи міцність і пружність тіла, він водночас є достатньо гнучким і м'яким, що має велике значення при виконанні різноманітних функцій.</p>
